package es.iespuertolacruz.almacen.api;
public class Zona {
 
    char idZona;
    String tipo;

    public Zona(char idZona, String tipo) {
        this.idZona = idZona;
        this.tipo = tipo;
    }

    public char getIdZona() {
        return this.getIdZona();
    }

    public String getTipo() {
        return this.tipo;
    }
}
